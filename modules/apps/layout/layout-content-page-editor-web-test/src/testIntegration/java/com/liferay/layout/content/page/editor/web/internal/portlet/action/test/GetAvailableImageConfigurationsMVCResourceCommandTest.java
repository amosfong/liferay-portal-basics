/**
 * @author Eudaldo Alonso
 */
@RunWith(Arquillian.class)
public class GetAvailableImageConfigurationsMVCResourceCommandTest {

	@ClassRule
	@Rule
	public static final AggregateTestRule aggregateTestRule =
		new AggregateTestRule(
			new LiferayIntegrationTestRule(),
			PermissionCheckerMethodTestRule.INSTANCE);

	@Before
	public void setUp() throws Exception {
		_group = GroupTestUtil.addGroup();
	}

	@Test
	public void testGetFileEntryWidth() throws Exception {
		FileEntry fileEntry = _dlAppLocalService.addFileEntry(
			null, TestPropsValues.getUserId(), _group.getGroupId(),
			DLFolderConstants.DEFAULT_PARENT_FOLDER_ID,
			RandomTestUtil.randomString() + ".jpg", ContentTypes.IMAGE_JPEG,
			FileUtil.getBytes(
				GetAvailableImageConfigurationsMVCResourceCommandTest.class,
				"dependencies/liferay.jpg"),
			null, null, null,
			ServiceContextTestUtil.getServiceContext(
				_group, TestPropsValues.getUserId()));

		int width = ReflectionTestUtil.invoke(
			_mvcResourceCommand, "_getFileEntryWidth",
			new Class<?>[] {FileEntry.class}, fileEntry);

		Assert.assertEquals(197, width);
	}

	@Inject
	private DLAppLocalService _dlAppLocalService;

	@DeleteAfterTestRun
	private Group _group;

	@Inject(
		filter = "mvc.command.name=/layout_content_page_editor/get_available_image_configurations"
	)
	private MVCResourceCommand _mvcResourceCommand;

}