/**
 * @author Gislayne Vitorino
 */
@RunWith(Arquillian.class)
public class ImageTableReferenceDefinitionTest
	extends BaseTableReferenceDefinitionTestCase {

	@ClassRule
	@Rule
	public static final AggregateTestRule aggregateTestRule =
		new AggregateTestRule(
			new LiferayIntegrationTestRule(),
			PermissionCheckerMethodTestRule.INSTANCE);

	@Override
	protected CTModel<?> addCTModel() throws Exception {
		return _imageLocalService.updateImage(
			TestPropsValues.getCompanyId(), _counterLocalService.increment(),
			FileUtil.getBytes(
				ImageTableReferenceDefinitionTest.class,
				"dependencies/liferay.png"));
	}

	@Inject
	private CounterLocalService _counterLocalService;

	@Inject
	private ImageLocalService _imageLocalService;

}